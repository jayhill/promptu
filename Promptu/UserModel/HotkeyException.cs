using System;
using System.Collections.Generic;
using System.Text;

namespace ZachJohnson.Promptu.UserModel
{
    [global::System.Serializable]
    public class HotkeyException : Exception
    {
        public HotkeyException()
        {
        }

        public HotkeyException(string message)
            : base(message)
        {
        }

        public HotkeyException(string message, Exception inner)
            : base(message, inner)
        {
        }

        protected HotkeyException(
          System.Runtime.Serialization.SerializationInfo info,
          System.Runtime.Serialization.StreamingContext context)
            : base(info, context)
        {
        }
    }
}
