using System;
using System.Collections.Generic;
using System.Text;

namespace ZachJohnson.Promptu.UserModel
{
    [global::System.Serializable]
    internal class ProfileLoadException : Exception
    {
        public ProfileLoadException()
        {
        }

        public ProfileLoadException(string message)
            : base(message)
        {
        }

        public ProfileLoadException(string message, Exception inner)
            : base(message, inner)
        {
        }

        protected ProfileLoadException(
          System.Runtime.Serialization.SerializationInfo info,
          System.Runtime.Serialization.StreamingContext context)
            : base(info, context)
        {
        }
    }
}
