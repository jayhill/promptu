namespace ZachJohnson.Promptu.UIModel
{
    internal struct CellAddress
    {
        int column;
        int row;

        public CellAddress(int row, int column)
        {
            this.column = column;
            this.row = row;
        }

        public int Column
        {
            get { return this.column; }
            set { this.column = value; }
        }

        public int Row
        {
            get { return this.row; }
            set { this.row = value; }
        }
    }
}
