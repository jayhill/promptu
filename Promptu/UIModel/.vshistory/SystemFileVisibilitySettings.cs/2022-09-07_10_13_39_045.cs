using System;
using System.Collections.Generic;
using System.Text;

namespace ZachJohnson.Promptu.UIModel
{
    internal class SystemFileVisibilitySettings
    {
        bool showSystemFiles;
        bool showHiddenFiles;

        public SystemFileVisibilitySettings(bool showSystemFiles, bool showHiddenFiles)
        {
            this.showSystemFiles = showSystemFiles;
            this.showHiddenFiles = showHiddenFiles;
        }

        public bool ShowSystemFiles
        {
            get { return this.showSystemFiles; }
        }

        public bool ShowHiddenFiles
        {
            get { return this.showHiddenFiles; }
        }
    }
}
